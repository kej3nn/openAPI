/**
 * 이벤트 게시판 내용을 등록한다.
 * 
 * @param button {Element} 버튼
 */
function insertEvent(button) {
    if ($(button).hasClass("opened")) {
        window.location.href = com.wise.help.url("/portal/etc/event/insertBulletinPage.do");
    }
    else {
        alert("이벤트 기간이 아닙니다.");
    }
}

