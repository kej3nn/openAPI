/**
 * 이벤트를 바인딩한다.
 */
function bindEvent() {
    // 이벤트 응모하기 버튼에 클릭 이벤트를 바인딩한다.
    $(".event-insert-button").bind("click", function(event) {
        // 이벤트 게시판 내용을 등록한다.
        insertEvent(this);
        return false;
    });
    
    // 이벤트 응모하기 버튼에 키다운 이벤트를 바인딩한다.
    $(".event-insert-button").bind("keydown", function(event) {
        if (event.which == 13) {
            // 이벤트 게시판 내용을 등록한다.
            insertEvent(this);
            return false;
        }
    });
}

