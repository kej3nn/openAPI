function bindEvent() {
	//탭 이벤트
	$("#tabs li").bind("click", function(e) {
		$("#tabs li").removeClass("on");
		$(this).addClass("on");
		
		var index = $(this).index()+1;
		$("div[id ^= \"contents_0\"]").hide();
		$("#contents_0"+index).show();
		
	});
	$("#provPop").bind("click", function(e) {
		var url = com.wise.help.url("/portal/exposeInfo/provisionPopupPage.do");
		OpenWindow(url, "provPop", "1024", "850", "yes");
	});
	
}

